var angka1 = 10;
var angka2 = 2;

let sum = (angka1 + angka2);
console.log("Sum:", sum);

let difference = (angka1 - angka2);
console.log("Difference:",difference);

let product = (angka1 * angka2);
console.log("Product:",product);

let quotient = (angka1 / angka2);
console.log("Quotient:",quotient);

let remainder = (angka1 % angka2);
console.log("Remainder:",remainder);

let increment = ++angka1;
console.log("Value of angka1 after increment:", increment);

let decrement = --angka2;
console.log("Value of angka2 after decrement:", decrement);